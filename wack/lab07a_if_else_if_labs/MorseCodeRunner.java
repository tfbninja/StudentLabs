public class MorseCodeRunner {

    public static void main(String args[]) {
        MorseCode test = new MorseCode('A');
        System.out.println(test);

        test.setChar('B');
        System.out.println(test);

        test.setChar('3');
        System.out.println(test);

        test.setChar('Z');
        System.out.println(test);

        test.setChar('8');
        System.out.println(test);

        test.setChar('F');
        System.out.println(test);

        test.setChar('0');
        System.out.println(test);
    }
}
